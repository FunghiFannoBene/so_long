#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		((unsigned char *)b)[i] = c;
		i++;
	}
	return (b);
}
/*non si puo' restituire un oggetto di tipo void. 
e quindi gli si assegna l'unsigned char. e' un cast.*/